package test_3;

public class FaturamentoDiario {
	private int dia;
	private double value;
	
	
	public FaturamentoDiario(int dia, double value) {
		this.dia = dia;
		this.value = value;
	}


	public int getDia() {
		return dia;
	}


	public void setDia(int dia) {
		this.dia = dia;
	}


	public double getValue() {
		return value;
	}


	public void setValue(double value) {
		this.value = value;
	}
	
	
	
	
	
}
